public class Paciente {
    String pacID;
    String pacNombre;

    public void setPacID(String pacID){
        this.pacID=pacID;
    }

    public void setPacNombre(String pacNombre){
        this.pacNombre=pacNombre;
    }

    public String getPacID(){
        return pacID;
    }

    public String getPacNombre(){
        return pacNombre;
    }
}
