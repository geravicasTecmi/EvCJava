public class Doctores {
    String docID;
    String docNombre;
    String docEspecialidad;

    public void setDocID (String docID){
        this.docID=docID;
    }

    public void setDocNombre (String docNombre){
        this.docNombre=docNombre;
    }

    public void setDocEspecialidad (String docEspecialidad){
        this.docEspecialidad=docEspecialidad;
    }

    public String getDocID (){
        return docID;
    }

    public String getDocNombre(){
        return docNombre;
    }

    public String getDocEspecialidad(){
        return docEspecialidad;
    }
}
